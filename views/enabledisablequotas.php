  <div class="grid-container-fluid" id="content-container">
    <section class="content-body grid-100">

      <div class="rm-Markdown markdown-body" data-testid="RDMD" style="margin-left:31px">
        <h2 class="heading heading-2 header-scroll" id="what-it-does">
          <div class="heading-text">What It Does?</div>
        </h2>
        <p>Quotas help balance or limit responses from different subgroups like age, gender, or other custom variables. You can:</p>
        <ul>
          <li>Set interlocked or nested quotas for combinations (e.g., 55–64 Males)</li>
          <li>Define percentages or fixed numbers</li>
          <li>Enable/Disable quotas at any time during setup</li>
        </ul>

        <h2 class="heading heading-2 header-scroll" id="how-to-enable">
          <div class="heading-text">How to Enable?</div>
        </h2>
        <p><strong>From Edit Audience:</strong></p>
        <ol>
          <li>Navigate to your project and click on the <strong>Edit Audience</strong> menu.</li>
          <li>Under the ⋯ (three-dot) options, select <strong>Interlock / Nest Quotas</strong>.</li>
          <li>In the <em>Add Interlock Quotas</em> screen:
            <ul>
              <li>Name your quota group.</li>
              <li>Choose <strong>Targeting Variables</strong> (like Age and Gender).</li>
              <li>Click <strong>Create new quota</strong> to generate quota rows.</li>
              <li>Use <strong>Calculate Feasibility</strong> to test before saving.</li>
            </ul>
          </li>
          <li>Click <strong>Save quotas</strong> to apply them to your audience.</li>
        </ol>

        <h3>Enable / Disable Quota Answers</h3>
        <ul>
          <li>While setting quota rows, click the ⋯ menu on any row.</li>
          <li>Choose <strong>Disable Quota</strong> to deactivate it temporarily.</li>
          <li>Use <strong>Delete</strong> to remove it entirely.</li>
        </ul>

        <h2 class="heading heading-2 header-scroll" id="fields">
          <div class="heading-text">Fields</div>
        </h2>
        <table>
          <thead>
            <tr>
              <th>Field</th>
              <th>Meaning</th>
              <th>Notes</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Quota Name</td>
              <td>Name for the interlocked group</td>
              <td>Shown on setup screen</td>
            </tr>
            <tr>
              <td>Targeting Vars</td>
              <td>Criteria like Age, Gender</td>
              <td>At least one required</td>
            </tr>
            <tr>
              <td>% or #</td>
              <td>Set by percentage or fixed count</td>
              <td>Click icon to toggle</td>
            </tr>
            <tr>
              <td>Disable Quota</td>
              <td>Temporarily turns off quota logic for a row</td>
              <td>Does not delete the row</td>
            </tr>
            <tr>
              <td>Create new quota</td>
              <td>Auto-generates all combinations of selected values</td>
              <td>Editable afterward</td>
            </tr>
          </tbody>
        </table>

        <h2 class="heading heading-2 header-scroll" id="how-it-behaves">
          <div class="heading-text">How It Behaves</div>
        </h2>
        <ul class="space-y-2">
          <li>Quotas stop collecting responses when the specified limit is reached.</li>
          <li>Disabled quotas are skipped during data collection.</li>
          <li>Can be combined with National Representative settings (if available).</li>
          <li>Quotas influence feasibility calculations.</li>
          <li>Only active quotas affect response distribution.</li>
        </ul>
