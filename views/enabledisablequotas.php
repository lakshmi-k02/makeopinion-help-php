        <h1>Quotas</h1>
        <div class="excerpt">
          <div class="rm-Markdown markdown-body" data-testid="RDMD">
            <p>Control how many responses you receive from specific segments of your target audience by setting demographic or behavioral quotas.</p>
          </div>
        </div>
      </div>
    </div>
