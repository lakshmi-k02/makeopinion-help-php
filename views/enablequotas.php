        <h1>Enable Quotas</h1>
        <div class="excerpt">
          <div class="rm-Markdown markdown-body" data-testid="RDMD">
            <p>Enable quotas to set limits on how many respondents can qualify under specific targeting combinations. This helps ensure that responses are collected evenly or according to specific demographic goals.</p>
          </div>
        </div>
      </div>
    </div>
  </header>

  <div class="grid-container-fluid" id="content-container">
    <section class="content-body grid-100">

      <div class="rm-Markdown markdown-body" data-testid="RDMD" style="margin-left:31px">

        <h2 class="heading heading-2 header-scroll" id="what-it-does">
          <div class="heading-text">What It Does</div>
        </h2>
        <ul>
          <li>Lets you define respondent distribution by <strong>age</strong>, <strong>gender</strong>, or any other targeting variables.</li>
          <li>Supports <strong>interlocked quotas</strong>—combinations of multiple variables (e.g., Male 55–64).</li>
          <li>Quotas can be set by <strong>percent</strong> or <strong>absolute number</strong>.</li>
          <li>Provides quota behavior control like disabling or deleting rows.</li>
        </ul>

        <h2 class="heading heading-2 header-scroll" id="how-to-enable">
          <div class="heading-text">How to Enable</div>
        </h2>
        <p><strong>From the Audience Panel:</strong></p>
        <ol>
          <li>Navigate to your project &gt; Click <strong>Edit Audience</strong>.</li>
          <li>From the top-right menu (⋯), select <strong>Interlock / Nest Quotas</strong>.</li>
          <li>Add a <strong>Quota Name</strong>.</li>
          <li>Select <strong>Targeting Variables</strong> (e.g., Age, Gender).</li>
          <li>Click <strong>Create New Quota</strong> to generate quota combinations.</li>
          <li>Adjust quota <strong>distribution (% or count)</strong> per row as needed.</li>
          <li>Click <strong>Save Quotas</strong>.</li>
        </ol>

        <h2 class="heading heading-2 header-scroll" id="fields">
          <div class="heading-text">Fields</div>
        </h2>
        <table>
          <thead>
            <tr>
              <th>Field</th>
              <th>Description</th>
              <th>Notes</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Quota Name</td>
              <td>Name of the interlocked quota group</td>
              <td>Internal only</td>
            </tr>
            <tr>
              <td>Targeting Variables</td>
              <td>Factors used to define the quota combinations</td>
              <td>E.g., Age + Gender</td>
            </tr>
            <tr>
              <td>% / # Fields</td>
              <td>Set quota by percentage or absolute number</td>
              <td>Each row can be edited</td>
            </tr>
            <tr>
              <td>Create New Quota</td>
              <td>Auto-generates all possible combinations from selected vars</td>
              <td>Optional but time-saving</td>
            </tr>
          </tbody>
        </table>

        <h2 class="heading heading-2 header-scroll" id="how-it-behaves">
          <div class="heading-text">How It Behaves</div>
        </h2>
        <ul class="space-y-2">
          <li>Quotas act as limits: once a row fills up, no more respondents qualify for that path.</li>
          <li>You can <strong>edit</strong> quotas anytime.</li>
          <li>Combined with other targeting and screening logic.</li>
          <li>Used to manage diversity and distribution of collected data.</li>
          <li>Can be paired with National Rep quotas (if configured at duplication stage).</li>
        </ul>

