                <h2 class="heading heading-2 header-scroll">
                    <div class="heading-anchor anchor waypoint" id="what-it-does"></div>
                    <div class="heading-text">
                        <div id="section-what-it-does" class="heading-anchor_backwardsCompatibility"></div>What It Does?
                    </div>
                    <a class="heading-anchor-icon fa fa-anchor" href="#what-it-does"></a>
                </h2>
                <p>Allows you to configure specific demographic or targeting settings for each audience, including quotas, location, and number of required completes.</p>

                <h2 class="heading heading-2 header-scroll">
                    <div class="heading-anchor anchor waypoint" id="how-to-enable"></div>
                    <div class="heading-text">
                        <div id="section-how-to-enable" class="heading-anchor_backwardsCompatibility"></div>How to Enable?
                    </div>
                    <a class="heading-anchor-icon fa fa-anchor" href="#how-to-enable"></a>
                </h2>
                <h4>From Create Survey:</h4>
                <ul>
                    <li>Click <strong>Start a Project</strong> in the upper-right corner of your account.</li>
                    <li>Choose one of the options: Get Survey Participants, Start a new survey, or Select Survey Templates.</li>
                    <li>Locate the audience panel and click the 3-dot menu (⋯) next to the audience name.</li>
                    <li>Select <strong>Edit Audience Settings</strong>.</li>
                </ul>

                <h4>From Edit Survey:</h4>
                <ul>
                    <li>Go to the project where the audience is listed (e.g., <em>2025 Thursday 05 custom_survey</em>).</li>
                    <li>Click the three vertical dots (⋯) beside the audience name.</li>
                    <li>Choose <strong>Edit Audience Settings</strong> from the dropdown.</li>
             <span style="display:block;">
  <img src="images/EditAudience.png" alt="edit audience" 
       style="margin-top: 10px; max-width: 60%; height:60%; border: 1px solid #ccc; border-radius: 4px; cursor: pointer; transition: box-shadow 0.2s;" 
       onclick="expandImage(this)">
</span>
                    <li>The <strong>Edit Audience</strong> panel will open on the right.</li>
                </ul>

                <h4>Modify General Settings:</h4>
                <ul>
                    <li>Update the <strong>Audience Name</strong> as needed.</li>
                    <li><strong>Country</strong> is visible but cannot be changed once set.</li>
                    <li>Set <strong>How many completes?</strong> using preset (200, 300, etc.) or custom input.</li>
                </ul>

                <h4>Adjust Additional Settings:</h4>
                <ul>
                    <li><strong>Field Duration</strong>: e.g., 1 day</li>
                    <li><strong>Length of Interview (LOI)</strong>: e.g., 0 min</li>
                    <li><strong>Incidence Rate</strong>: e.g., 50%</li>
                    <li><strong>CPI (Cost Per Interview)</strong>: e.g., $0.32</li>                          
                </ul>
               
                <h4>Save Changes:</h4>
                <ul>
                    <li>Click <strong>Save Changes</strong> to keep your edits without applying them yet.</li>
                    <li>Click <strong>Apply Changes</strong> to make them live.</li>
                                                <span style="display:block">
