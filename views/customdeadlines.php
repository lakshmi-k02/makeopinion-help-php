        <h2 class="heading heading-2 header-scroll" id="what-it-does">
          <div class="heading-text">What It Does</div>
        </h2>
        <ul>
          <li>Allows you to set custom project start and end times.</li>
          <li>Permits defining individual deadlines for specific audience groups.</li>
          <li>Supports flexibility in fieldwork planning across multiple time zones and client needs.</li>
        </ul>

        <h2 class="heading heading-2 header-scroll" id="how-to-enable">
          <div class="heading-text">How to Set Custom Deadlines</div>
        </h2>

        <h4>1. Access Your Project</h4>
        <ul>
          <li>From the <strong>Dashboard</strong>, open the project for which you want to manage deadlines.</li>
        </ul>

        <h4>2. Navigate to Custom Deadlines</h4>
        <ul>
          <li>On the project page, locate and click on the <strong>Custom Deadlines</strong> button (usually at the bottom or in the project controls).</li>
          <span style="display:block;">
          <img src="images/CustomDeadline.png" alt="Custom Deadlines"
            style="margin-top: 10px; max-width: 60%; height:60%; border: 1px solid #ccc; border-radius: 4px; cursor: pointer; transition: box-shadow 0.2s;"
            onclick="expandImage(this)">
        </span>
        </ul>

        <h4>3. Configure Dates</h4>
        <ul>
          <li>Set the <strong>Project Start Date</strong> and <strong>Project End Date</strong> using the date and time pickers.</li>
          <li>Enable the <strong>Use Custom Deadlines</strong> checkbox to assign deadlines per audience.</li>
          <li>Enter specific deadlines for each audience if desired.</li>
        </ul>

        <h4>4. Save Settings</h4>
        <ul>
          <li>Click <strong>Save</strong> to apply changes to your project timelines.</li>
        </ul>

