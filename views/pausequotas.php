                <h2 class="heading heading-2 header-scroll" id="how-to-enable">
                    <div class="heading-text">Why Pause a Quota?</div>
                </h2>
                <p>Imagine you are targeting older men and women vs. young people across Berlin and Munich. Let's say:</p>
                <ul>
                    <li>Older men in Berlin are difficult to reach.</li>
                    <li>Young people and older women in Munich fill up fast.</li>
                    <li>The quota will remain visible but no longer collect responses until re-enabled</li>
                </ul>

                <p>If you don’t pause the easier segments, you might accidentally exhaust your entire Munich quota with older women or young people, leaving no room to fulfill the harder Berlin-old-men segment.</p>

                <p>By pausing the faster-filling groups, you:</p>
                 <ul>
                    <li>Protect space for the harder segments</li>
                    <li>Ensure balanced and complete quotas</li>
                    <li>Prevent oversampling from easy regions/demographics</li>
                </ul>
