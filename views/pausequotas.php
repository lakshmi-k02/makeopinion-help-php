                <h1>Pause Quota</h1>
                <div class="excerpt">
                    <div class="rm-Markdown markdown-body" data-testid="RDMD">
                        <p>Sometimes, hitting overall quota numbers too quickly, especially for easier-to-reach groups, can create imbalances. The Pause Quota feature allows you to temporarily stop collecting responses for specific quota cells while continuing to target others. This gives you more strategic control over difficult or location-sensitive segments.</p>
                    </div>
