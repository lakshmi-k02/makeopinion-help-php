                <h2 class="heading heading-2 header-scroll" id="how-to-enable">
                    <div class="heading-text">How to Pause a Quota</div>
                </h2>
                <ul>
                    <li>Click the ⋮ three-dot menu next to the quota row.</li>
                    <li>Select Disable Quota to pause it.</li>
                    <li>The quota will remain visible but no longer collect responses until re-enabled</li>
                </ul>
