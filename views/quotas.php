                <h2 class="heading heading-2 header-scroll" id="how-it-behaves">
                    <div class="heading-text">How It Behaves</div>
                </h2>
                <ul class="space-y-2 modern-list">
                    <li>Quotas allow you to control the distribution of responses across categories (e.g., making sure 20% are 18–24, 30% are 25–34).</li>
                    <li>Toggle Quotas ON to activate balancing, while Targeting ON ensures only the selected ranges are included.</li>
                    <li>Enter targets manually as numbers or percentages, or apply national representativity tools to auto-balance.</li>
                    <li>The system tracks completion against each quota during fielding.</li>
                    <li>Once a quota fills, no more respondents from that category are admitted.</li>
                    <li>This ensures your dataset matches the demographic balance you require.</li>
                </ul>
