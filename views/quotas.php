                <h2 class="heading heading-2 header-scroll" id="how-to-enable">
                    <div class="heading-text">Steps</div>
                </h2>
                <ul>
                    <li><strong>Open the audience</strong> and click on the screening question you want to set quotas for (e.g., <strong>How old are you?</strong>).</li>
                    <li>Toggle <strong>Quotas ON</strong> to enable quota balancing.</li>
                    <li>Define your targets by entering either a <strong>percentage</strong> or <strong>absolute number</strong> for each category or range.</li>
                    <li>(Optional) Add new custom ranges or use <strong>National Representativity functions</strong> for automated, balanced splits.</li>
                    <li>Click <strong>Save</strong> to apply the quota settings.</li>
                </ul>
