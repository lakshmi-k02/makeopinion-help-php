        <h2 class="heading heading-2 header-scroll" id="difference-between-clicks-and-completes">
          <div class="heading-text">Difference Between Clicks and Completes</div>
        </h2>
        <ul>
          <li><strong>Clicks</strong>: Counts every time a participant starts the survey, regardless of whether they finish it.</li>
          <li><strong>Completes</strong>: Counts only fully completed surveys that meet your criteria.</li>
        </ul>

        <h2 class="heading heading-2 header-scroll" id="how-it-works">
          <div class="heading-text">How It Works</div>
        </h2>
        <ol>
          <li>You set a <strong>Soft Launch limit</strong> (e.g., 30 completes).</li>
          <li>As responses come in, the system tracks them based on your selection (<strong>Clicks</strong> or <strong>Completes</strong>).</li>
          <li>Once the limit is reached:
            <ul>
              <li>The project <strong>pauses automatically</strong>.</li>
              <li>You receive both an <strong>in-platform notification</strong> and an <strong>email alert</strong>.</li>
            </ul>
          </li>
          <li>Review the data from the Soft Launch to check quality, incidence, and LOI.</li>
          <li>To proceed, you must <strong>manually resume</strong> the project to go fully live.</li>
          <li>Note: <strong>Soft Launch can only be used once per project</strong>.</li>
        </ol>

