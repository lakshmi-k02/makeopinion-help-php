        <h2 class="heading heading-2 header-scroll" id="how-it-works">
          <div class="heading-text">How It Works</div>
        </h2>
        <ol>
          <li>Custom screeners allow you to ask <strong>unique qualification questions</strong> not found in the standard library.</li>
          <li>These appear <strong>before the main survey</strong> and filter participants who can continue.</li>
          <li><strong>Targeting ON</strong> ensures only respondents meeting criteria can enter the survey.</li>
          <li><strong>Quotas</strong> let you balance responses across answer choices.</li>
          <li><strong>Translation</strong> ensures the question is localized for the audienceâ€™s language.</li>
          <li>Adding too many strict screeners can reduce feasibility and increase cost.</li>
        </ol>
