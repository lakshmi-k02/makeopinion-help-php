        <h1>Audience Detail View</h1>
        <div class="excerpt">
          <div class="rm-Markdown markdown-body" data-testid="RDMD">
            <p>The Audience Detail View provides a comprehensive overview and management interface for individual survey audiences within a project. It allows users to monitor key metrics, configure specific screening questions, and control the launch status of each audience.</p>
