        <h2 class="heading heading-2 header-scroll" id="fields">
          <div class="heading-text">Fields</div>
        </h2>
        <table>
          <thead>
            <tr>
              <th>Field / Section</th>
              <th>Description</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td><strong>Audience Header</strong></td>
              <td>Displays the Audience ID, Name, and associated country flag.</td>
            </tr>
            <tr>
              <td><strong>Key Metrics Summary</strong></td>
              <td>Shows critical project data for the specific audience (e.g., Requested Completes, LOI, CPI, Remaining Cost).</td>
            </tr>
            <tr>
              <td><strong>Launch Status &amp; Control</strong></td>
              <td>Indicates the current state of the audience (<code>Pending Launch</code>, <code>Live</code>, <code>Paused</code>) and provides action options (Launch, Pause, Edit, etc.).</td>
            </tr>
            <tr>
              <td><strong>Screening Questions</strong></td>
              <td>Lists all active screening questions applied to this audience, with an option to <code>Add Screening Question</code>.</td>
            </tr>
            <tr>
              <td><strong>Navigation &amp; Management Controls</strong></td>
              <td>Includes top-level buttons and dropdowns for managing audiences (e.g., <code>Add New Audience</code>, <code>Select Audience</code>, <code>View Options</code>).</td>
            </tr>
          </tbody>
        </table>

        <h2 class="heading heading-2 header-scroll" id="how-it-behaves">
          <div class="heading-text">How It Behaves</div>
        </h2>
        <ul class="space-y-2">
          <li>This view provides a dedicated space to manage the granular details of a single audience.</li>
          <li>Key metrics are dynamically updated based on the audience configuration and ongoing data collection.</li>
          <li>Users can add and review both standard and custom screening questions directly within this view, which filter respondents <em>before</em> they enter the main survey.</li>
          <li>The launch status of individual audiences can be controlled (launched, paused) via the action menu, independent of other audiences in the project.</li>
          <li>Warnings and alerts are displayed if there are setup issues or potential conflicts with the audience definition.</li>
        </ul>

