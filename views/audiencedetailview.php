        <h2 class="heading heading-2 header-scroll" id="how-to-enable">
          <div class="heading-text">How to Enable?</div>
        </h2>
        <ul>
          <li>Click <strong>Start a Project</strong> in the upper-right corner of your account.</li>
          <li>Choose one of the options: Get Survey Participants, Start a new survey, or Select Survey Templates.</li>
        </ul>
        <p><strong>From the Audience List:</strong></p>
        <ol>
          <li>Navigate to your project's main view, which displays the <strong>Audience List</strong>.</li>
          <li>Click on the specific audience name from the list to open its detailed view.</li>
        </ol>
