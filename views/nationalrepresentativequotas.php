        <h1>National Representative Quotas</h1>
        <div class="excerpt">
          <div class="rm-Markdown markdown-body" data-testid="RDMD">
            <p>National Representative Quotas auto-apply demographic balancing based on official statistics (e.g., census data). These reflect age, gender, and regional splits of a population.</p>
          </div>
        </div>
      </div>
    </div>
  </header>

  <div class="grid-container-fluid" id="content-container">
    <section class="content-body grid-100">

      <div class="rm-Markdown markdown-body" data-testid="RDMD" style="margin-left:31px">

        <h2 class="heading heading-2 header-scroll" id="what-it-does">
          <div class="heading-text">What It Does</div>
        </h2>
        <ul>
          <li>Automatically aligns your sample with the general population's demographic structure.</li>
          <li>Uses preset quotas based on <strong>age</strong>, <strong>gender</strong>, and <strong>region</strong>.</li>
          <li>Reduces the need to configure interlocked quotas manually.</li>
        </ul>

        <h2 class="heading heading-2 header-scroll" id="how-to-enable">
          <div class="heading-text">How to Enable</div>
        </h2>
        <p><strong>From the Duplicate Audience Panel:</strong></p>
        <ol>
          <li>Go to <strong>Audience Panel</strong> &gt; Click on <strong>â‹¯ (More Options)</strong> &gt; Select <strong>Duplicate Audience</strong>.</li>
          <li>Choose the <strong>target country</strong> (e.g., Germany).</li>
          <li>Under <strong>Copy Standard Screeners</strong>:
            <ul>
              <li>Set <strong>Standard Screeners Source</strong> to:<br>
                <code>National representative distribution by age, gender, and region</code></li>
              <li>(Optional) Tick <strong>Include Quotas Distribution</strong> to apply preset splits.</li>
            </ul>
          </li>
          <li>Click <strong>Duplicate Audience</strong> to finalize.</li>
        </ol>

        <h2 class="heading heading-2 header-scroll" id="fields">
          <div class="heading-text">Fields</div>
        </h2>
        <table>
          <thead>
            <tr>
              <th>Field</th>
              <th>Description</th>
              <th>Notes</th>
            </tr>
          </thead>
          <tbody>
            <tr>
              <td>Standard Screeners Source</td>
              <td>Preset logic to balance demographics</td>
              <td>Must be set to "National Rep"</td>
            </tr>
            <tr>
              <td>Include Quotas Distribution</td>
              <td>Whether to apply hard-coded quota splits</td>
              <td>Optional</td>
            </tr>
          </tbody>
        </table>

        <h2 class="heading heading-2 header-scroll" id="how-it-behaves">
          <div class="heading-text">How It Behaves</div>
        </h2>
        <ul class="space-y-2">
          <li>National Rep quotas are <strong>automatically structured</strong>.</li>
          <li>These can be edited later in <strong>Interlocked Quotas</strong>.</li>
          <li>Ensures consistency across multi-country studies.</li>
          <li>Simplifies project setup for common sampling needs.</li>
        </ul>

        <h2 class="heading heading-2 header-scroll" id="notes">
          <div class="heading-text">Notes</div>
        </h2>
        <ul>
          <li>Available only in supported countries with census benchmarks.</li>
          <li>Often used for syndicated or tracking research.</li>
          <li>Cannot be combined with manual quota logic unless edited afterward.</li>
        </ul>

