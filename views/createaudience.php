          <div class="heading-text">How It Behaves</div>
        </h2>
        <ul>
          <li>Creates a scoped audience group tied to the project.</li>
          <li>Can be duplicated and customized later.</li>
          <li>Acts as a foundation for applying further targeting and quota settings.</li>
